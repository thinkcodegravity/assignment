package Inheritance;

/*
	Write a program for calcualator.
		1. Method add that takes 2 int input and returns int.
		2. Method sub that takes 2 int input and returns int.
		3. Method mul that takes 2 int input and returns int.
		4. Method div that takes 2 int input and returns int.
*/

public class Calculator {

	public int add(int a, int b) {
		return a + b;
	}

	public int sub(int a, int b) {
		return a - b;
	}

	public int mul(int a, int b) {
		return a * b;
	}

	public int div(int a, int b) {
		return a / b;
	}

}
