package Inheritance1;
/*
 * Class Calculator does calculation. this program can perform addition, subtraction , multiplication and division.
 */
public class Calculator {

	//method for addition
	public int add(int a, int b) {
		int sum=a+b;
		return sum;
	}
	//method for subtraction
	public int sub(int a, int b) {
		int sub=a-b;
		return sub;
	}
	//method for multiplication
	public int mul(int a, int b) {
		int mul=a*b;
		return mul;
	}
	//method for division
	public int div(int a, int b) {
		int div=a/b;
		return div;
	}


}

