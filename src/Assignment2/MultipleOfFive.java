/*
 * This program is created to give the multiple of 5 from 1 to 10
 */
package Assignment2;

public class MultipleOfFive {

	//Main Method
	public static void main(String[] args) {
		System.out.println("The muliple of 5:");
		
		//Using for loop to calculate the multiple of 5 using increment
		for(int i=1; i<=10; i++)
		{
			
			System.out.println("5 * "+i+" = "+(5*i));
		}
		

	}

}
