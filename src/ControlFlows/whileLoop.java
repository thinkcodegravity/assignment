package ControlFlows;

public class whileLoop {

	public static void main(String[] args) {
		//Multiply of 6 between 1 to 10
		//Using the while loop
		//Multiply table for Multiplications
		System.out.println("Multiply of 6 between 1 to 10 are;");
		int number=6;
		int i =1;
		while (i<=10) {
			System.out.println(number+"*"+i+"="+number*i);
			i++;
		}
		
	}
	
}
		